#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*temp;

	if (!lst || !f)
		return ;
	while (lst != NULL)
	{
		temp = lst->next;
		f(lst);
		lst = temp;
	}
}
